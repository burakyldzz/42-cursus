#include "libft.h"

int	ft_atoi(const char *str)
{
	int	result;
	int	i;
	int	negative;

	i = 0;
	result = 0;
	negative = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negative *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		result = result * 10;
		result += str[i] - '0';
		i++;
	}
	return (result * negative);
}
